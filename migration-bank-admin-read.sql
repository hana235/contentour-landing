--       직접 송금할 수 있도록 admin SELECT 정책을 명시적으로 보장.
-- 성격: 멱등(DROP IF EXISTS 후 CREATE). 재실행 안전. is_admin() 헬퍼는 이미 존재.
--       기존 '계좌_본인_조회'에 admin OR이 포함돼 있을 수 있으나(중복은 OR로 무해),
--       라이브 정책이 본인-only일 가능성을 없애기 위해 명시적 admin 읽기를 추가.
-- ════════════════════════════════════════════════════════════════

ALTER TABLE "41_계좌정보" ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "계좌_admin_조회" ON "41_계좌정보";
CREATE POLICY "계좌_admin_조회" ON "41_계좌정보" FOR SELECT TO authenticated
USING (public.is_admin());

