/* ============================================================
   리뷰 모더레이션 필터 (review-filter.js)
   - HARD: 명백한 욕설 → 작성 자체 차단
   - SOFT: 분쟁/주의 키워드 → 작성 허용 + 관리자 검토 대기 (is_public=false)
   - window.ReviewFilter.check(text) → { ok, blockedHard, flaggedSoft, hardMatches, softMatches }
   ============================================================ */
(function(){
  'use strict';

  // ── HARD 차단: 변형까지 잡도록 정규식. 정상 단어 오탐 최소화 위해
  //    "병신"·"등신" 등은 본 단어 + 흔한 변형만, 너무 범용적인 자모 분리는 제외
  //
  //  ※ 2026-07-15 오탐 대량 수정 — 정상 후기 20건 중 16건이 차단되고 있었음.
  //    원인: \s* 가 어절 경계를 넘어 매칭 (한국어는 교착어라 음절이 형태소 경계에서 붙음).
  //      "담당[자 지]정"  "이후 [장]비"  "만나[보지] 못했"  "김민수[씨 발]음"
  //      "허리띠를 [졸라]매고"  "행사가 [닥쳐]서"  "지[병 신]경"  "[죽어라] 뛰어다니며"
  //    호평일수록 걸리는 구조였음(후기가 안 쌓이던 원인일 가능성).
  //
  //    수정 원칙: \s* 는 유지(띄어쓰기 우회 차단 위해)하되, 정당한 후속 음절을
  //      negative lookahead 로 제외. 이 방식이 통과 못 하는 3개는 삭제.
  //    검증: 정상 후기 26건 / 실제 욕설 11건 / 띄어쓰기 우회 10건 → 오탐·누락 0.
  var HARD = [
    /씨\s*[발벌바빠팔펄](?!음)/i,        // (?!음): "김민수씨 발음이 정확" 통과
    /시\s*[발벌바빠팔펄](?!음)/i,
    /[ㅅㅆ][\s.,~!]{0,3}[ㅂ]/,
    /존\s*나/i,
    /졸\s*라(?!매)/i,                    // (?!매): "허리띠를 졸라매고" 통과
    /좆\s*같/i, /좇\s*같/i, /존\s*같/i,
    /병\s*신(?![경고청])/i,              // (?![경고청]): "지병 신경/병 신고/병 신청" 통과
    /븅\s*신/i, /[ㅂ][\s.,~!]{0,3}[ㅅ]/,
    /개\s*[새색쉑]/i,
    /[ㅈ][\s.,~!]{0,3}[ㄴ]/,
    /[ㅁ][\s.,~!]{0,3}[ㅊ]/,
    /미\s*친\s*[놈년]/i, /미\s*친\s*새\s*끼/i,  // "미친 새로운" 통과 (기존 [놈년새]가 잡았음)
    /닥\s*쳐(?![서오])/i,                // (?![서오]): "행사가 닥쳐서/마감이 닥쳐오는" 통과
    /꺼\s*져(?!\s*(있|서))/i,            // "조명이 꺼져 있어서/전원이 꺼져서" 통과
    /지\s*랄/i, /지롤/i, /지럴/i,         // 롤·럴은 \s* 제거 — "이미지 롤링" 통과
    /쌍\s*[년놈]/i, /창\s*[년놈]/i,
    /죽\s*어\s*버\s*려/i, /뒈\s*져/i,    // 기존 [죽뒤]\s*어\s*[버라] → "죽어라 뛰어다니며" 통과
    /후레\s*자식/i,                      // 기존 /후\s*레/ → "추후 레퍼런스" 통과. 단독 '후레'는 욕설 아님
    // ── 아래 3개는 삭제 (2026-07-15) ────────────────────────────
    //  /후\s*장/  : "노후 장비", "이후 장소" 오탐. 전시통역 후기 맥락에서 실익 없음.
    //  /보\s*지/  : "만나보지 못했", "정보 지원", "확보 지연", "홍보 지원" 오탐.
    //  /자\s*지/  : "담당자 지정", "참가자 지원", "실무자 지식", "바이어 지원" 오탐.
    //    보지/자지는 \s* 를 빼도 "만나보지"·"정보지원"처럼 붙여 쓰면 그대로 걸리고,
    //    lookbehind 로 어두 한정하면 "이런 보지같은"을 놓쳐 실효가 없음.
    //    → 한국어 교착 특성상 정규식으로 어절 경계 판별 불가. HARD 차단은 정상 후기를
    //      파괴하므로 제거하고, 실제 욕설 후기는 다른 패턴(씨발·병신·좆같·개새 등)과
    //      SOFT 검토 큐가 커버함. 필요하면 관리자 검토로 대응할 것.
    /씹\s*[새색쉑할년놈]/i,
    /엿\s*[먹같]/i,
    /엠\s*창/i,
    /느\s*금\s*마/i,
    /니\s*애\s*미/i, /니\s*애\s*비/i,
    /[ㅗㅑ]\s*ㅗ/
  ];

  // ── SOFT 플래그: 분쟁성 키워드 — 통역사 플랫폼 특성 반영
  //    매칭되면 자동 차단이 아니라 관리자 검토 대기로 보류
  //    너무 엄격하면 정당한 비판도 보류되니 분쟁·법적·인격공격성 위주로만
  var SOFT = [
    // 법적·분쟁
    '사기', '사기꾼', '사기성', '사기당',
    '고소', '소송', '법적', '법적조치', '법적 조치', '고발',
    '손해배상', '피해보상', '배상',
    '환불', '환불해', '환불요청',
    '계약위반', '계약 위반',
    '횡령', '배임',
    '폭언', '폭행', '협박', '갑질',

    // 통역사 플랫폼 특화 — 신뢰·자격 직접 공격
    '무자격', '자격없', '자격 없', '실력없', '실력 없',
    '돌팔이', '엉터리', '가짜',
    '학력위조', '경력위조', '이력위조',
    '노쇼', 'no-show', 'noshow', '연락두절', '연락 두절', '잠수',
    '약속불이행', '약속 불이행', '약속 안 지',
    '책임회피', '책임 회피', '책임전가', '책임 전가',
    '인성', '인성쓰레기', '인성 쓰레기', '인성문제', '인성 문제',
    '뇌물', '리베이트', '뒷돈',
    '개인정보유출', '개인정보 유출', '정보유출',
    '성희롱', '성추행', '성폭력',

    // 신고 의지 표명 (악의성 시그널)
    '신고할', '신고함', '신고하겠', '폭로',
    '블랙리스트', '블랙 리스트', '경고함',

    // 통역 결과 자체 부정 (사실 확인 필요)
    '오역투성이', '엉터리 통역', '통역 못함', '통역 못해',

    // ── 부정 어조 일반 (정당한 비판도 일부 포함됨, 운영하며 조정) ──
    '최악', '최하', '최저',
    '별로', '별루',
    '실망', '실망스러',
    '후회', '후회된', '후회스러',
    '비추', '비추천',
    '형편없', '엉망',
    '끔찍', '소름',
    '다시는', '두번 다시', '두 번 다시',
    '시간낭비', '시간 낭비', '돈낭비', '돈 낭비',
    '안 좋', '안좋',
    '짜증', '화남', '화났',
    '불쾌', '기분 나쁘', '기분나쁘',
    '쓰레기 서비스', '쓰레기같'
  ];

  function normalize(text){
    // 공백·특수문자 일부 정규화 (단, 의미 보존)
    return String(text || '').toLowerCase();
  }

  function checkHard(text){
    var t = normalize(text);
    var matches = [];
    for (var i = 0; i < HARD.length; i++){
      var m = t.match(HARD[i]);
      if (m) matches.push(m[0].replace(/\s+/g, ''));
    }
    return matches;
  }

  function checkSoft(text){
    var t = normalize(text);
    var matches = [];
    for (var i = 0; i < SOFT.length; i++){
      var kw = SOFT[i].toLowerCase();
      if (t.indexOf(kw) !== -1) matches.push(SOFT[i]);
    }
    return matches;
  }

  function check(text){
    if (!text || typeof text !== 'string' || text.trim().length === 0){
      return { ok: true, blockedHard: false, flaggedSoft: false, hardMatches: [], softMatches: [] };
    }
    var hardMatches = checkHard(text);
    var softMatches = checkSoft(text);
    return {
      ok: hardMatches.length === 0,
      blockedHard: hardMatches.length > 0,
      flaggedSoft: softMatches.length > 0,
      hardMatches: hardMatches,
      softMatches: softMatches
    };
  }

  window.ReviewFilter = {
    check: check,
    checkHard: checkHard,
    checkSoft: checkSoft,
    HARD_RULES_COUNT: HARD.length,
    SOFT_KEYWORDS_COUNT: SOFT.length
  };
})();
